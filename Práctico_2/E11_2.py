digit = int(input("Ingresa un numero de un digito: "))

if digit == 0:
    print("Cero.")
elif digit == 1:
    print("Uno.")
elif digit == 2:
    print("Dos.")
elif digit == 3:
    print("Tres.")
elif digit == 4:
    print("Cuatro.")
elif digit == 5:
    print("Cinco.")
elif digit == 6:
    print("Seis.")
elif digit == 7:
    print("Siete.")
elif digit == 8:
    print("Ocho.")
elif digit == 9:
    print("Nueve.")
else:
    print("Su numero tiene mas de un digito.")